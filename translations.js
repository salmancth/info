// Language management
let currentLanguage = localStorage.getItem('language') || 'en';

function updateTextContent(lang) {
    document.documentElement.lang = lang;
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Update title if it has data-translate
    const title = document.querySelector('title');
    if (title && title.getAttribute('data-translate')) {
        const key = title.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            title.textContent = translations[lang][key];
        }
    }
    
    // Update meta descriptions
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && metaDesc.getAttribute('data-translate')) {
        const key = metaDesc.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            metaDesc.setAttribute('content', translations[lang][key]);
        }
    }
}

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    updateTextContent(lang);
    updateLanguageToggle();
}

function toggleLanguage() {
    const newLang = currentLanguage === 'en' ? 'sv' : 'en';
    setLanguage(newLang);
}

function updateLanguageToggle() {
    const toggles = document.querySelectorAll('.language-toggle');
    toggles.forEach(toggle => {
        toggle.classList.toggle('active', currentLanguage === 'sv');
    });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    updateTextContent(currentLanguage);
    updateLanguageToggle();
    
    // Add event listeners to language toggle buttons
    document.querySelectorAll('.language-toggle').forEach(toggle => {
        toggle.addEventListener('click', toggleLanguage);
    });
});

// Export for use in other files
window.translations = translations;
window.setLanguage = setLanguage;
window.toggleLanguage = toggleLanguage;
window.updateTextContent = updateTextContent;